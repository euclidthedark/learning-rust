fn main () {
    /*
     * There are 4 scaler types:
     * 1) Bool
     * 2) Ints - Can be signed or unsigned
     * 3) Chars
     * 4) And double floats
     * */

    let is_a_bool = true;

    let an_integer: i8 = 10;

    let a_char = 'a';

    let a_float: f32 = 4.5;

    println!("{}, {}, {}, {}", is_a_bool, an_integer, a_char, a_float);

}
